<?php
/* 
 * this formats a query string param for url querys
 */
class QSParam{
    protected $strName;
    protected $strValue;
    public function  __construct($strName, $strValue) {
        $this->strName = $strName;
        $this->strValue = $strValue;
    }
    public function ToString(){
        return sprintf('%s=%s', $this->strName, $this->strValue);
    }
}
?>
